# GeneGPT Final Answer Structure (Template)

# Every GeneGPT answer must follow this structure:

# 1. summary
#    - Short explanation of the gene or variant (2–3 simple lines)

# 2. evidence
#    - omim:
#         - key points
#         - inheritance
#         - disease names
#         - OMIM ID
#         - link
#    - ncbi:
#         - basic gene function (1–2 lines)
#         - location
#         - link
#    - pubmed:
#         - top 2–3 papers
#         - pmid, title, year, 1–2 sentence summary, link
#    - clinvar (only for variants):
#         - clinical significance
#         - condition
#         - review status
#         - ClinVar accession ID
#         - link

# 3. sources_used
#    - list of sources that had useful data

# 4. sources_empty
#    - list of sources that had no data

# 5. confidence
#    - high / medium / low

# This template is the fixed shape of every GeneGPT answer.
